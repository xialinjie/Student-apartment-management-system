    private void signupBTMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signupBTMouseReleased
        // TODO add your handling code here:
        String username = usernameSignup.getText();
        String password = passwordSignup.getText();
        boolean flag = true;
        if (username == null || username.trim().length() == 0) {
            usernameSignupHint.setText("* Username cannot be empty");
            flag = false;
        }
        if (password == null || password.trim().length() == 0) {
            passwordSignupHint.setText("* Password cannot be empty");
            flag = false;
        }

        if (flag) {
            int result = DataHandler.signup(usernameSignup.getText(), passwordSignup.getText());
            if (result != -1) {
                uidPage = result;
                usernamePage = username;
                setMenuBackground(accountMenu, homeMenu, accommodationMenu, dailyitemsMenu, color1, color2);
                jumpPages(accountPage, homePage, accommodationPage, dailyitemsPage, loginandsignupPage);
                usernameLogin.setText("");
                passwordLogin.setText("");
                usernameSignup.setText("");
                passwordSignup.setText("");
                setAccountMessage();
            } else {
                passwordLoginHint.setText("* Wrong password");
            }

        }
         
    }//GEN-LAST:event_signupBTMouseReleased

