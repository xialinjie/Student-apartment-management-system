    private void loginBTMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBTMouseReleased
        // TODO add your handling code here:
        String username = usernameLogin.getText();
        String password = passwordLogin.getText();
        boolean flag = true;
        if (username == null || username.trim().length() == 0) {
            usernameLoginHint.setText("* Username cannot be empty");
            flag = false;
        }
        if (password == null || password.trim().length() == 0) {
            passwordLoginHint.setText("* Password cannot be empty");
            flag = false;
        }

        if (username != null && username.trim().length() != 0) {
            int result = DataHandler.checkUsernameLogin(username);
            if (result == -1) {
                usernameLoginHint.setText("* The user does not exist");
                flag = false;
            } else {
                usernameLoginHint.setText(" ");
            }
        }
        if (flag) {
            int result = DataHandler.checkLogin(usernameLogin.getText(), passwordLogin.getText());
            if (result != -1) {
                uidPage = result;
                usernamePage = username;

                setMenuBackground(accountMenu, homeMenu, accommodationMenu, dailyitemsMenu, color1, color2);
                jumpPages(accountPage, homePage, accommodationPage, dailyitemsPage, loginandsignupPage);
                usernameLogin.setText("");
                passwordLogin.setText("");
                usernameSignup.setText("");
                passwordSignup.setText("");
                setAccountMessage();
            } else {
                passwordLoginHint.setText("* Wrong password");
            }

        }
    }//GEN-LAST:event_loginBTMouseReleased

